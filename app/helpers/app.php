function redirect($url, $exit = false, $time = false)
{
    if ($time) header('Refresh: ' . $time . '; url= ' . $url);
    else header('Location: ' . $url);

    if($exit) exit;
}

function session($name, $value = false)
{
    if ($value) {
        $_SESSION[$name] = $value;
    } else {
        if (is_array($name)) {
            foreach ($name as $key => $val) {
                if ($key && $val) session(htmlspecialchars($key), htmlspecialchars($val));
            }
        } else return isset($_SESSION[$name]) ? $_SESSION[$name] : false;
    }
    return false;
}

function uniqidReal($lenght = 16) {
    if (function_exists("random_bytes")) {
        $bytes = random_bytes(ceil($lenght / 2));
    } elseif (function_exists("openssl_random_pseudo_bytes")) {
        $bytes = openssl_random_pseudo_bytes(ceil($lenght / 2));
    } else {
        throw new Exception("no cryptographically secure random function available");
    }
    return substr(bin2hex($bytes), 0, $lenght);
}

function str_random($str = '',$length = 11)
{
    $characters = '0123456789abcdefghijklmnopqrstuvwxyz0123456789ABCDEFGHIJKLMNOPQRSTUVWXYZ';
    $charactersLength = strlen($characters);
    $randomString = '';
    for ($i = 0; $i < $length; $i++) {
        $randomString .= ($i == 2) ? $str : $characters[rand(0, $charactersLength - 1)];
    }
    return $randomString;
}

function mapper($arr = [], $key = '', $val = '')
{
    if (((is_array($arr) && count($arr) <= 0) || !is_array($arr)) || empty($key) || empty($val)) return false;
    $rarr = [];
    foreach ($arr as $k) {
        $rarr[$k[$key]] = $k[$val];
    }
    return $rarr;
}

