function route($name = false, $params = [])
{
	global $Route;
	if($name){
		$route = $Route->getByName($name);
		if(!empty($params)){
			if(is_array($params)){
				foreach($params as $param)
					$route['uri'] = preg_replace('/'.preg_quote('([a-z0-9A-Z\_\-]+)', '/').'/', $param, $route['uri'], 1);
			}elseif(!is_callable($params) && !is_bool($params) && !is_array($params) && !is_object($params)){
				$route['uri'] = preg_replace('/'.preg_quote('([a-z0-9A-Z\_\-]+)', '/').'/', $params, $route['uri'], 1);
				if(strpos($route['uri'], '([a-z0-9A-Z\_\-]+)') !== FALSE){
					$route['uri'] = rtrim(str_replace('([a-z0-9A-Z\_\-]+)', '', $route['uri']), '/');
				}
			}
		}
		return $route ? url()->base($route['uri']) : false;
	}else 
		return $Route;
}

function db()
{
