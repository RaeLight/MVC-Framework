}

function request_method($method, $reject = true){
	return ((is_string($method) && $_SERVER['REQUEST_METHOD'] == $method && $reject) || 
	   (is_string($method) && $_SERVER['REQUEST_METHOD'] != $method && $reject == false) || 
	   (is_array($method) && in_array($_SERVER['REQUEST_METHOD'], $method) && $reject) || 
	   (is_array($method) && !in_array($_SERVER['REQUEST_METHOD'], $method) && $reject == false));
}

