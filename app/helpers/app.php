        return new basicdb($GLOBALS['config']['db']['host'], 
        $GLOBALS['config']['db']['name'], 
        $GLOBALS['config']['db']['user'], 
        $GLOBALS['config']['db']['pass']);
    } catch (\Throwable $th) {
        throw $th;
